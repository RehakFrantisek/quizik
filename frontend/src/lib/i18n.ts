/** Look up a translation key, fall back to English, then to the key itself. */
export function tr(lang: string, key: string): string {
  return translations[lang]?.[key] ?? translations["en"]?.[key] ?? key;
}

/**
 * Returns a properly pluralized question count label.
 * Handles Czech 1/2-4/5+ plural forms.
 */
export function questionCountLabel(lang: string, n: number): string {
  if (lang === "cs") {
    if (n === 1) return "1 otázka";
    if (n >= 2 && n <= 4) return `${n} otázky`;
    return `${n} otázek`;
  }
  return n === 1 ? "1 question" : `${n} questions`;
}
